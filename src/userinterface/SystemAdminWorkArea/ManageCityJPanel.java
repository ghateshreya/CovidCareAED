    private void populateCityTable() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        
        for(City city: ecosystem.getCityList()){
            Object[] row = new Object[1];
            row[0] = city.getName();
            dtm.addRow(row);
        }
    }
